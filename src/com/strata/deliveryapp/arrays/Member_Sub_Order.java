package com.strata.deliveryapp.arrays;

public class Member_Sub_Order {

	private String order_book_no;
	private String order_date;
	private String order_no;
	private String order_title;
	private String order_type;
	private Boolean id;
	
	public Boolean getId() {
		return id;
	}
	public void setId(Boolean id) {
		this.id = id;
	}
	public String getOrder_book_no() {
		return order_book_no;
	}
	public void setOrder_book_no(String order_book_no) {
		this.order_book_no = order_book_no;
	}
	public String getOrder_date() {
		return order_date;
	}
	public void setOrder_date(String order_date) {
		this.order_date = order_date;
	}
	public String getOrder_no() {
		return order_no;
	}
	public void setOrder_no(String order_no) {
		this.order_no = order_no;
	}
	public String getOrder_title() {
		return order_title;
	}
	public void setOrder_title(String order_title) {
		this.order_title = order_title;
	}
	public String getOrder_type() {
		return order_type;
	}
	public void setOrder_type(String order_type) {
		this.order_type = order_type;
	}
}
